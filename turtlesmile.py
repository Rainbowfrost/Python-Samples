import turtle
my_pen = turtle.Turtle()      

def eye(rad):
    my_pen.down()
    my_pen.circle(rad)
    my_pen.up()

my_pen.fillcolor('blue')
my_pen.begin_fill()
my_pen.circle(100)
my_pen.end_fill
my_pen.up

my_pen.goto(-40, 120)
my_pen.down
my_pen.circle(10)
my_pen.up

my_pen.goto(40, 120)
my_pen.down
my_pen.circle(10)
my_pen.up

my_pen.goto(0, 0)
my_pen.down